using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using FlutterSharp.CodeGen.Models;
using Scriban;

namespace FlutterSharp.CodeGen.Generators.Dart
{
	/// <summary>
	/// Generates Dart enum definitions from enum definitions.
	/// </summary>
	public class DartEnumGenerator
	{
		private readonly Template _template;

		/// <summary>
		/// Initializes a new instance of the <see cref="DartEnumGenerator"/> class.
		/// </summary>
		public DartEnumGenerator()
		{
			// Load the Scriban template
			var templatePath = Path.Combine(AppDomain.CurrentDomain.BaseDirectory, "Templates", "DartEnum.scriban");
			if (File.Exists(templatePath))
			{
				var templateContent = File.ReadAllText(templatePath);
				_template = Template.Parse(templateContent);
			}
			else
			{
				// Use inline template as fallback
				_template = Template.Parse(GetDefaultTemplate());
			}
		}

		/// <summary>
		/// Generates a Dart enum definition from an enum definition.
		/// </summary>
		/// <param name="enumDef">The enum definition to generate from.</param>
		/// <returns>The generated Dart enum code.</returns>
		public string Generate(EnumDefinition enumDef)
		{
			if (enumDef == null)
			{
				throw new ArgumentNullException(nameof(enumDef));
			}

			// Prepare the model for the template
			var model = new
			{
				enumDef.Name,
				EnumName = enumDef.Name,
				Values = enumDef.Values.Select((v, index) => new
				{
					Name = ToCamelCase(v.Name),
					OriginalName = v.Name,
					Value = v.Value ?? index,
					v.Documentation,
					v.IsDeprecated,
					IsLast = index == enumDef.Values.Count - 1
				}).ToList(),
				Documentation = enumDef.Documentation,
				enumDef.IsDeprecated,
				GeneratedDate = DateTime.UtcNow.ToString("yyyy-MM-dd HH:mm:ss UTC"),
				Namespace = enumDef.Namespace
			};

			// Render the template
			var result = _template.Render(model);
			return result;
		}

		/// <summary>
		/// Converts a PascalCase string to camelCase.
		/// </summary>
		private string ToCamelCase(string value)
		{
			if (string.IsNullOrEmpty(value) || char.IsLower(value[0]))
			{
				return value;
			}

			return char.ToLowerInvariant(value[0]) + value.Substring(1);
		}

		/// <summary>
		/// Converts a camelCase string to PascalCase.
		/// </summary>
		private string ToPascalCase(string value)
		{
			if (string.IsNullOrEmpty(value) || char.IsUpper(value[0]))
			{
				return value;
			}

			return char.ToUpperInvariant(value[0]) + value.Substring(1);
		}

		/// <summary>
		/// Gets the default template if the template file is not found.
		/// </summary>
		private string GetDefaultTemplate()
		{
			return @"// <auto-generated>
// This file was automatically generated on {{ generated_date }}
// DO NOT EDIT - Changes will be overwritten
// </auto-generated>

{{~ if documentation ~}}
/// {{ documentation }}
{{~ end ~}}
{{~ if is_deprecated ~}}
@Deprecated('This enum is deprecated')
{{~ end ~}}
enum {{ enum_name }} {
{{~ for value in values ~}}
{{~ if value.documentation ~}}
  /// {{ value.documentation }}
{{~ end ~}}
{{~ if value.is_deprecated ~}}
  @Deprecated('This value is deprecated')
{{~ end ~}}
  {{ value.name }}{{~ if !value.is_last ~}},{{~ end ~}}
{{~ end ~}}
}

/// Extension methods for {{ enum_name }}
extension {{ enum_name }}Extension on {{ enum_name }} {
  /// Converts the enum value to an integer.
  int toInt() {
    switch (this) {
{{~ for value in values ~}}
      case {{ enum_name }}.{{ value.name }}:
        return {{ value.value }};
{{~ end ~}}
    }
  }

  /// Creates an enum value from an integer.
  static {{ enum_name }}? fromInt(int value) {
    switch (value) {
{{~ for value in values ~}}
      case {{ value.value }}:
        return {{ enum_name }}.{{ value.name }};
{{~ end ~}}
      default:
        return null;
    }
  }
}
";
		}
	}
}
