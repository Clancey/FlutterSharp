using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using FlutterSharp.CodeGen.Models;

namespace FlutterSharp.CodeGen.Generators.Dart
{
	/// <summary>
	/// Generates Dart parser imports file for DynamicWidgetBuilder.
	/// </summary>
	public class DartParserImportsGenerator
	{
		/// <summary>
		/// Generates a complete parser registration file with imports and parser list.
		/// </summary>
		/// <param name="widgets">The widget definitions to generate imports and list for.</param>
		/// <param name="skipWidgets">Optional set of widget names to skip (widgets without generated parsers).</param>
		/// <returns>The generated Dart code as a string.</returns>
		public string Generate(IEnumerable<WidgetDefinition> widgets, HashSet<string>? skipWidgets = null)
		{
			if (widgets == null)
			{
				throw new ArgumentNullException(nameof(widgets));
			}

			skipWidgets ??= new HashSet<string>(StringComparer.OrdinalIgnoreCase);

			var sb = new StringBuilder();

			// Header
			sb.AppendLine("// <auto-generated>");
			sb.AppendLine($"// This file was automatically generated on {DateTime.UtcNow:yyyy-MM-dd HH:mm:ss} UTC");
			sb.AppendLine("// DO NOT EDIT - Changes will be overwritten");
			sb.AppendLine("// </auto-generated>");
			sb.AppendLine();

			// Known abstract base classes that won't have parser files generated
			var abstractBaseClasses = new HashSet<string>(StringComparer.OrdinalIgnoreCase)
			{
				"StatelessWidget",
				"StatefulWidget",
				"RenderObjectWidget",
				"ProxyWidget",
				"ParentDataWidget",
				"InheritedWidget",
				"LeafRenderObjectWidget",
				"SingleChildRenderObjectWidget",
				"MultiChildRenderObjectWidget",
				"SlottedRenderObjectWidget",
				"AnimatedWidget",
				"BoxScrollView",
				"ConstrainedLayoutBuilder",
				"ImplicitlyAnimatedWidget",
				"InheritedModel",
				"InheritedNotifier",
				"InheritedTheme",
				"ScrollView",
				"SliverMultiBoxAdaptorWidget",
				"StreamBuilderBase",
				"AbstractLayoutBuilder",
				"SliverWithKeepAliveWidget",
				"SlottedMultiChildRenderObjectWidget",
				"InspectorButton",
				"StatusTransitionWidget",
				"TwoDimensionalScrollView",
				"TwoDimensionalViewport",
				"UniqueWidget"
			};

			// Sort widgets by name for consistent output.
			// Filter out private widgets (starting with _), abstract base classes, and explicitly skipped widgets.
			// Distinct by name to prevent duplicate parser imports when multiple libraries expose the same widget type.
			var sortedWidgets = widgets
				.Where(w => !w.Name.StartsWith("_") && !w.IsAbstract && !abstractBaseClasses.Contains(w.Name) && !skipWidgets.Contains(w.Name))
				.GroupBy(w => w.Name, StringComparer.OrdinalIgnoreCase)
				.Select(g => g.First())
				.OrderBy(w => w.Name, StringComparer.OrdinalIgnoreCase)
				.ToList();

			// Generate imports
			sb.AppendLine("// Generated parser imports");
			foreach (var widget in sortedWidgets)
			{
				var fileName = widget.Name.ToLowerInvariant() + "_parser.dart";
				sb.AppendLine($"import 'parsers/{fileName}';");
			}

			sb.AppendLine();
			sb.AppendLine("// Generated parser instances - add to DynamicWidgetBuilder._parsers list");
			sb.AppendLine("// Note: These are stub implementations that return placeholders.");
			sb.AppendLine("// Manual implementations in /parsers directory should be used when available.");
			sb.AppendLine("final generatedParsers = [");

			// Generate parser instantiations
			foreach (var widget in sortedWidgets)
			{
				var parserName = $"{widget.Name}Parser";
				sb.AppendLine($"  {parserName}(),");
			}

			sb.AppendLine("];");

			return sb.ToString();
		}

	}
}
