	/// <summary>
	/// Generates Dart utility parser functions for converting FFI types to Dart/Flutter types.
	/// These are the parse* functions like parseInt, parseColor, parseAlignment, etc.
	/// </summary>
	public class DartUtilityParserGenerator
	{
		private readonly CSharpToDartFfiMapper _typeMapper;
		private readonly Template _primitiveTemplate;
		private readonly Template _enumTemplate;
		private readonly Template _structTemplate;
		private readonly Template _stubTemplate;
		private List<TypeDefinition> _typeDefinitions = new();

		public DartUtilityParserGenerator(CSharpToDartFfiMapper typeMapper)
		{
			_typeMapper = typeMapper ?? throw new ArgumentNullException(nameof(typeMapper));

			// Parse templates
			_primitiveTemplate = Template.Parse(GetPrimitiveTemplate());
			_enumTemplate = Template.Parse(GetEnumTemplate());
			_structTemplate = Template.Parse(GetStructTemplate());
			_stubTemplate = Template.Parse(GetStubTemplate());
		}

		/// <summary>
		/// Generates all missing parser functions based on TypeMappingRegistry.
		/// </summary>
	public string GenerateAll(List<EnumDefinition> enums, List<TypeDefinition> types, HashSet<string> existingParsers)
	{
		_typeDefinitions = types ?? new List<TypeDefinition>();
		var sb = new StringBuilder();

		sb.AppendLine("// <auto-generated>");
		sb.AppendLine($"// This file was automatically generated on {DateTime.UtcNow:yyyy-MM-dd HH:mm:ss} UTC");
		sb.AppendLine("// DO NOT EDIT - Changes will be overwritten");
		sb.AppendLine("// Contains generated parser utility functions");
		sb.AppendLine("// </auto-generated>");
		sb.AppendLine();
		sb.AppendLine("import 'dart:ffi' hide Size;");
		sb.AppendLine("import 'dart:collection';  // For Set/HashSet");
		sb.AppendLine("import 'dart:ui';");
		sb.AppendLine("import 'package:ffi/ffi.dart';");
		sb.AppendLine("import 'package:flutter/material.dart';");
		sb.AppendLine("import 'package:flutter/widgets.dart';");
		sb.AppendLine("import 'package:flutter/gestures.dart';  // For gesture callbacks");
		sb.AppendLine("import 'package:flutter/rendering.dart';  // For PlatformViewHitTestBehavior, PointerSignalEventListener, etc.");
		sb.AppendLine("import 'package:flutter/services.dart';  // For PointerEnterEventListener, PlatformViewCreatedCallback, etc.");
		sb.AppendLine("import 'package:flutter_module/mauiRenderer.dart' show methodChannel;  // For callback invocation");
		sb.AppendLine("import '../flutter_sharp_structs.dart';");
		sb.AppendLine();

		// Add imports for all struct types used in parsers
		// Collect unique struct types from the parsers that will be generated
		sb.AppendLine("// Struct imports");
		var structTypes = new HashSet<string>(StringComparer.OrdinalIgnoreCase);
		var registeredParsersForImports = GetRegisteredParsers();
		foreach (var (parserName, mapping) in registeredParsersForImports)
		{
			// Skip if already exists
			if (existingParsers.Contains(parserName))
			{
				continue;
			}

			// Skip complex types that shouldn't have parsers
			if (ShouldSkipParser(mapping.DartType, mapping.CSharpType))
			{
				continue;
			}

			// Check if this will generate a struct parser
			if (HasStructDefinition(mapping.DartType))
			{
				var baseType = mapping.DartType?.TrimEnd('?');
				var structType = $"{baseType}Struct";

				// Don't import WidgetStruct (it's in flutter_sharp_structs.dart)
				if (structType != "WidgetStruct")
				{
					structTypes.Add(structType);
				}
			}
		}

		// Additional struct types used by GenerateAdditionalHelpers() but not in registered parsers
		structTypes.Add("TextStyleStruct");

		// Structs defined in flutter_sharp_structs.dart (not in separate files)
		var baseFileStructs = new HashSet<string>(StringComparer.OrdinalIgnoreCase)
		{
			"FlutterObjectStruct", "WidgetStruct", "ChildrenStruct",
			"AlignmentStruct", "ColorStruct", "AppBarStruct", "CheckboxStruct",
			"EdgeInsetGemoetryStruct", "DefaultTabControllerStruct",
			"ListViewBuilderStruct", "ScaffoldStruct", "TextFieldStruct"
		};

		foreach (var structType in structTypes.OrderBy(s => s))
		{
			// Skip structs that are already in flutter_sharp_structs.dart
			if (baseFileStructs.Contains(structType))
			{
				continue;
			}

			// Convert SomeTypeStruct -> sometype_struct.dart
			var typeName = structType.Replace("Struct", "");
			var structFileName = $"{typeName.ToLowerInvariant()}_struct.dart";
			sb.AppendLine($"import 'structs/{structFileName}';");
		}
		sb.AppendLine();

		// Get all registered parser functions from TypeMappingRegistry
		var registeredParsers = GetRegisteredParsers();
		Console.WriteLine($"[DEBUG] Found {registeredParsers.Count} registered parser functions");
		Console.WriteLine($"[DEBUG] Existing parsers: {existingParsers.Count}");

		// Generate parsers for each type
		int generatedCount = 0;
		int skippedCount = 0;
		foreach (var (parserName, mapping) in registeredParsers)
		{
			// Skip if already exists
			if (existingParsers.Contains(parserName))
			{
				continue;
			}

			// Skip complex types that shouldn't have parsers
			if (ShouldSkipParser(mapping.DartType, mapping.CSharpType))
			{
				skippedCount++;
				continue;
			}

			var generated = GenerateParser(parserName, mapping, enums);
			if (!string.IsNullOrEmpty(generated))
			{
				sb.AppendLine(generated);
				sb.AppendLine();
				generatedCount++;
			}
		}

		Console.WriteLine($"[DEBUG] Generated {generatedCount} new parser functions");
		Console.WriteLine($"[DEBUG] Skipped {skippedCount} complex type parsers");

		// Add additional commonly-used helper functions
		sb.AppendLine(GenerateAdditionalHelpers());

		// Add callback parsing functions
		sb.AppendLine(GenerateCallbackParsers());

		return sb.ToString();
	}

	/// <summary>
	/// Determines if a parser should be skipped for complex types.
	/// Only generate parsers for value types like geometry, colors, borders, etc.
	/// Skip widgets, controllers, delegates, and other complex framework objects.
	/// </summary>
	private bool ShouldSkipParser(string dartType, string csharpType)
	{
		if (string.IsNullOrEmpty(dartType)) return true;
		
		var baseType = dartType.TrimEnd('?');
		
		// Skip these specific types
		var skipTypes = new HashSet<string>(StringComparer.OrdinalIgnoreCase)
		{
			"dynamic", "Object", "Widget", "StatefulWidget", "StatelessWidget",
			"BuildContext", "State", "Key", "GlobalKey", "LocalKey",
			"ValueKey", "ObjectKey", "UniqueKey",
			"GestureRecognizer", "DragStartBehavior",
			"SemanticsProperties", "TextInputFormatter",
			// Skip callback types - they're generated by GenerateCallbackParsers() with proper implementation
			"VoidCallback", "ValueChanged"
		};

		if (skipTypes.Contains(baseType))
		{
			return true;
		}

		// Skip types ending with these suffixes (controllers, delegates, providers, etc.)
		var skipSuffixes = new[]
		{
			"Controller", "Delegate", "Provider", "Observer", "Listener",
			"Route", "Navigator", "Router", "Animation", "Curve",
			"Notification", "Intent", "Action", "Listenable", "ChangeNotifier",
			"Recognizer", "Formatter", "Properties"
		};
		
		foreach (var suffix in skipSuffixes)
		{
			if (baseType.EndsWith(suffix, StringComparison.OrdinalIgnoreCase))
			{
				return true;
			}
		}
		
		return false;
	}

		/// <summary>
		/// Generates a single parser function based on its type.
		/// </summary>
		private string GenerateParser(string parserName, TypeMappingRecord mapping, List<EnumDefinition> enums)
		{
			var dartType = mapping.DartType;
			var csharpType = mapping.CSharpType;
			var ffiType = mapping.DartStructType;

			// Determine parser category
			if (IsPrimitiveType(dartType))
			{
				return GeneratePrimitiveParser(parserName, dartType, ffiType);
			}
			else if (IsEnumType(dartType, enums))
			{
				var enumDef = enums.FirstOrDefault(e => e.Name == dartType || e.Name == dartType?.TrimEnd('?'));
				return GenerateEnumParser(parserName, enumDef);
			}
			else if (HasStructDefinition(dartType))
			{
				return GenerateStructParser(parserName, dartType, ffiType);
			}
			else
			{
				// Generate stub for complex types that need manual implementation
				return GenerateStubParser(parserName, dartType, csharpType);
			}
		}

		private bool IsPrimitiveType(string dartType)
		{
			if (string.IsNullOrEmpty(dartType)) return false;
			var baseType = dartType.TrimEnd('?');
			return baseType is "String" or "int" or "double" or "bool" or "num" or "Duration" or "DateTime";
		}

		private bool IsEnumType(string dartType, List<EnumDefinition> enums)
		{
			if (string.IsNullOrEmpty(dartType)) return false;
			var baseType = dartType.TrimEnd('?');
			return enums.Any(e => e.Name == baseType);
		}

		private bool HasStructDefinition(string dartType)
		{
			if (string.IsNullOrEmpty(dartType)) return false;
			var baseType = dartType.TrimEnd('?');

			// Types that have struct definitions
			var knownStructTypes = new HashSet<string>(StringComparer.OrdinalIgnoreCase)
			{
				"Size", "Offset", "Rect", "Radius", "Matrix4",
				"Alignment", "AlignmentGeometry",
				"EdgeInsets", "EdgeInsetsGeometry",
				"BorderRadius", "BorderRadiusGeometry",
				"Color",
				// Additional types with struct definitions
				"BoxConstraints", "Constraints",
				"Decoration", "BoxDecoration", "ShapeDecoration",
				"TextStyle",
				"Gradient", "LinearGradient", "RadialGradient", "SweepGradient"
			};

			return knownStructTypes.Contains(baseType);
		}

		private string GeneratePrimitiveParser(string parserName, string dartType, string ffiType)
		{
			var model = new
			{
				ParserName = parserName,
				DartType = dartType,
				FfiType = ffiType,
				IsInt = dartType?.Contains("int") == true,
				IsDouble = dartType?.Contains("double") == true,
				IsBool = dartType == "bool",
				IsString = dartType == "String",
				IsDuration = dartType == "Duration",
				IsDateTime = dartType == "DateTime"
			};

			return _primitiveTemplate.Render(model);
		}

	private string GenerateEnumParser(string parserName, EnumDefinition enumDef)
	{
		if (enumDef == null) return GenerateStubParser(parserName, "Enum", "Enum");

		var model = new
		{
			ParserName = parserName,
			EnumName = enumDef.Name,
			Values = enumDef.Values.Select((v, index) => new
			{
				Name = v.Name,
				CamelName = ToCamelCase(v.Name),
				Value = v.Value ?? index  // Use explicit value if available, otherwise use index
			}).ToList(),
			DefaultValue = ToCamelCase(enumDef.Values.FirstOrDefault()?.Name ?? "unknown")
		};

		return _enumTemplate.Render(model);
	}

		private string GenerateStructParser(string parserName, string dartType, string ffiType)
		{
			var baseType = dartType?.TrimEnd('?');
			var structType = $"{baseType}Struct";

			// Try to find the type definition
			var typeDef = _typeDefinitions.FirstOrDefault(t =>
				t.Name.Equals(baseType, StringComparison.OrdinalIgnoreCase));

			// Get the constructor and properties
			var constructor = typeDef?.Constructors?.FirstOrDefault();
			var properties = typeDef?.Properties ?? new List<PropertyDefinition>();

			// Determine conversion strategy
			var conversionCode = GenerateStructConversion(baseType, structType, constructor, properties);

			var model = new
			{
				ParserName = parserName,
				DartType = dartType,
				StructType = structType,
				IsNullable = dartType?.EndsWith("?") == true,
				HasImplementation = !string.IsNullOrEmpty(conversionCode),
				ConversionCode = conversionCode
			};

			return _structTemplate.Render(model);
		}

		private string GenerateStructConversion(string dartType, string structType, ConstructorDefinition? constructor, List<PropertyDefinition> properties)
		{
			// Manual mappings for common types
			switch (dartType)
			{
				case "Color":
					return "Color.fromARGB(struct.alpha, struct.red, struct.green, struct.blue)";
				case "Alignment":
					return "Alignment(struct.x, struct.y)";
				case "AlignmentGeometry":
					// Abstract base class - For now return Alignment.center as fallback
					// TODO: C# side should send concrete Alignment type instead
					return "Alignment.center /* TODO: AlignmentGeometry is abstract */";
				case "Size":
					return "Size(struct.width, struct.height)";
				case "Offset":
					return "Offset(struct.dx, struct.dy)";
				case "Rect":
					return "Rect.fromLTRB(struct.left, struct.top, struct.right, struct.bottom)";
				case "Radius":
					return "Radius.elliptical(struct.x, struct.y)";
				case "EdgeInsets":
					return "EdgeInsets.fromLTRB(struct.left, struct.top, struct.right, struct.bottom)";
				case "EdgeInsetsGeometry":
					// Abstract base class - For now return EdgeInsets.zero as fallback
					// TODO: C# side should send concrete EdgeInsets type instead
					return "EdgeInsets.zero /* TODO: EdgeInsetsGeometry is abstract */";
				case "BorderRadius":
					// Parse each corner radius from Pointer<RadiusStruct>
					// Check pointer address instead of has flags for FFI struct compatibility
					return @"BorderRadius.only(
    topLeft: struct.topLeft.address != 0 ? parseRadius(Pointer<RadiusStruct>.fromAddress(struct.topLeft.address).ref) : Radius.zero,
    topRight: struct.topRight.address != 0 ? parseRadius(Pointer<RadiusStruct>.fromAddress(struct.topRight.address).ref) : Radius.zero,
    bottomLeft: struct.bottomLeft.address != 0 ? parseRadius(Pointer<RadiusStruct>.fromAddress(struct.bottomLeft.address).ref) : Radius.zero,
    bottomRight: struct.bottomRight.address != 0 ? parseRadius(Pointer<RadiusStruct>.fromAddress(struct.bottomRight.address).ref) : Radius.zero
  )";
				case "BorderRadiusGeometry":
					// Abstract base class - return BorderRadius.zero as fallback
					return "BorderRadius.zero /* TODO: BorderRadiusGeometry is abstract */";
				case "Matrix4":
					// Convert 16-element double array to Matrix4
					// Check pointer address instead of has flags for FFI struct compatibility
					return @"() {
    if (struct.storage.address == 0) return Matrix4.identity();
    var storage = struct.storage.cast<Double>();
    return Matrix4(
      storage[0], storage[1], storage[2], storage[3],
      storage[4], storage[5], storage[6], storage[7],
      storage[8], storage[9], storage[10], storage[11],
      storage[12], storage[13], storage[14], storage[15]
    );
  }()";
				case "BoxConstraints":
				case "Constraints":
					// Parse BoxConstraints from struct with min/max width/height
					return "BoxConstraints(minWidth: struct.minWidth, maxWidth: struct.maxWidth, minHeight: struct.minHeight, maxHeight: struct.maxHeight)";
				case "Decoration":
				case "BoxDecoration":
					// Decoration is abstract - return BoxDecoration with available properties
					// The struct should have color, borderRadius, etc.
					return "const BoxDecoration() /* TODO: implement full BoxDecoration parsing */";
				case "ShapeDecoration":
					// ShapeDecoration requires a shape parameter
					return "const BoxDecoration() /* TODO: ShapeDecoration not yet supported */";
				case "TextStyle":
					// TextStyle has many properties - use a helper function
					return "parseTextStyleFromStruct(struct)";
				case "Gradient":
				case "LinearGradient":
				case "RadialGradient":
				case "SweepGradient":
					// Gradient types are complex - throw for now since abstract Gradient can't have a simple default
					return null; // Will generate UnimplementedError throw
				default:
					// For other types, try to auto-generate if we have constructor info
					return null;
			}
		}

		private string GenerateStubParser(string parserName, string dartType, string csharpType)
		{
			var model = new
			{
				ParserName = parserName,
				DartType = dartType ?? "dynamic",
				CSharpType = csharpType ?? "object",
				IsCallback = parserName.ToLower().Contains("callback") || parserName.ToLower().Contains("function"),
				IsController = parserName.ToLower().Contains("controller"),
				IsComplex = true
			};

			return _stubTemplate.Render(model);
		}

		private Dictionary<string, TypeMappingRecord> GetRegisteredParsers()
		{
			var parsers = new Dictionary<string, TypeMappingRecord>();

			// Get the TypeMappingRegistry from the mapper
			var registryField = _typeMapper.GetType().GetField("_registry",
				System.Reflection.BindingFlags.NonPublic | System.Reflection.BindingFlags.Instance);

			if (registryField != null)
			{
				var registry = registryField.GetValue(_typeMapper) as TypeMappingRegistry;
				if (registry != null)
				{
					var allMappings = registry.GetAllMappings();
					foreach (var mapping in allMappings)
					{
						if (!string.IsNullOrEmpty(mapping.DartParserFunction))
						{
							parsers[mapping.DartParserFunction] = mapping;
						}
					}
				}
			}

			return parsers;
		}

		/// <summary>
		/// Converts a PascalCase string to camelCase.
		/// Also strips the @ prefix that C# uses for reserved word escaping (not valid in Dart).
		/// </summary>
		private string ToCamelCase(string value)
		{
			if (string.IsNullOrEmpty(value))
			{
				return value;
			}

			// Strip @ prefix (C# reserved word escaping) - not valid in Dart
			if (value.StartsWith("@"))
			{
				value = value.Substring(1);
			}

			if (char.IsLower(value[0]))
			{
				return value;
			}

			return char.ToLowerInvariant(value[0]) + value.Substring(1);
		}

