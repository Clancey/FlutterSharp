		private string GetPrimitiveTemplate()
		{
			return @"/// Parses {{ dart_type }} from FFI type.
{{~ if is_int ~}}
int {{ parser_name }}(int value) {
  return value;
}
{{~ else if is_double ~}}
double {{ parser_name }}(double value) {
  return value;
}
{{~ else if is_bool ~}}
bool {{ parser_name }}(int value) {
  return value != 0;
}
{{~ else if is_string ~}}
String? {{ parser_name }}(Pointer<Utf8> ptr) {
  if (ptr.address == 0) return null;
  return ptr.toDartString();
}
{{~ else if is_duration ~}}
Duration {{ parser_name }}(int milliseconds) {
  return Duration(milliseconds: milliseconds);
}
{{~ else if is_date_time ~}}
DateTime {{ parser_name }}(int millisecondsSinceEpoch) {
  return DateTime.fromMillisecondsSinceEpoch(millisecondsSinceEpoch);
}
{{~ else ~}}
{{ dart_type }} {{ parser_name }}(dynamic value) {
  return value as {{ dart_type }};
}
{{~ end ~}}";
		}

		private string GetEnumTemplate()
		{
			return @"/// Parses {{ enum_name }} from int value.
{{ enum_name }} {{ parser_name }}(int value) {
  switch (value) {
{{~ for v in values ~}}
    case {{ v.value }}:
      return {{ enum_name }}.{{ v.camel_name }};
{{~ end ~}}
    default:
      return {{ enum_name }}.{{ default_value }};
  }
}";
		}

		private string GetStructTemplate()
		{
			return @"/// Parses {{ dart_type }} from {{ struct_type }}.
{{~ if is_nullable ~}}
{{ dart_type }} {{ parser_name }}(Pointer<{{ struct_type }}> ptr) {
  if (ptr.address == 0) return null;
  var struct = ptr.ref;
{{~ if has_implementation ~}}
  return {{ conversion_code }};
{{~ else ~}}
  // TODO: Implement conversion from {{ struct_type }} to {{ dart_type }}
  throw UnimplementedError('{{ parser_name }} is not yet implemented');
{{~ end ~}}
}
{{~ else ~}}
{{ dart_type }} {{ parser_name }}({{ struct_type }} struct) {
{{~ if has_implementation ~}}
  return {{ conversion_code }};
{{~ else ~}}
  // TODO: Implement conversion from {{ struct_type }} to {{ dart_type }}
  throw UnimplementedError('{{ parser_name }} is not yet implemented');
{{~ end ~}}
}
{{~ end ~}}";
		}

		private string GetStubTemplate()
		{
			return @"/// Parses {{ dart_type }}.
/// TODO: This parser needs manual implementation.
{{~ if is_callback ~}}
// Callback parsers need special handling for FFI function pointers
{{~ else if is_controller ~}}
// Controller parsers typically return cached instances or create new ones
{{~ end ~}}
{{ dart_type }}? {{ parser_name }}(dynamic value) {
  // TODO: Implement {{ parser_name }}
  // CSharp type: {{ c_sharp_type }}
  print('WARNING: {{ parser_name }} is not implemented yet');
  return null;
}";
		}

