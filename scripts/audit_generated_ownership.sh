#!/usr/bin/env bash
set -euo pipefail

ROOT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
ENFORCE=0
SCOPE="${OWNERSHIP_SCOPE:-workspace}" # tracked|workspace

CSHARP_THRESHOLD="${CSHARP_THRESHOLD:-85}"
DART_THRESHOLD="${DART_THRESHOLD:-70}"

for arg in "$@"; do
  case "$arg" in
    --enforce)
      ENFORCE=1
      ;;
    --scope=tracked)
      SCOPE="tracked"
      ;;
    --scope=workspace)
      SCOPE="workspace"
      ;;
    *)
      echo "Unknown argument: $arg"
      echo "Usage: $0 [--enforce] [--scope=tracked|--scope=workspace]"
      exit 2
      ;;
  esac
done

if [[ "$SCOPE" != "tracked" && "$SCOPE" != "workspace" ]]; then
  echo "Invalid OWNERSHIP_SCOPE: $SCOPE (expected tracked or workspace)"
  exit 2
fi

compute_stats() {
  local dir="$1"
  local glob="$2"
  local no_ignore="$3"

  local total
  local generated
  local manual
  local ratio

  if [[ "$no_ignore" == "1" ]]; then
    total="$(rg --files --no-ignore "$dir" -g "$glob" | wc -l | tr -d ' ')"
    generated="$(rg -l '^// <auto-generated>' --no-ignore "$dir" -g "$glob" | wc -l | tr -d ' ')"
  else
    total="$(rg --files "$dir" -g "$glob" | wc -l | tr -d ' ')"
    generated="$(rg -l '^// <auto-generated>' "$dir" -g "$glob" | wc -l | tr -d ' ')"
  fi
  manual=$((total - generated))

  if [[ "$total" -eq 0 ]]; then
    ratio="0.0"
  else
    ratio="$(awk "BEGIN { printf \"%.1f\", ($generated / $total) * 100 }")"
  fi

  printf '%s %s %s %s\n' "$total" "$generated" "$manual" "$ratio"
}

print_row() {
  local label="$1"
  local total="$2"
  local generated="$3"
  local manual="$4"
  local ratio="$5"
  printf '%-38s total=%-6s generated=%-6s manual=%-6s ratio=%s%%\n' \
    "$label" "$total" "$generated" "$manual" "$ratio"
}

echo "Generated Ownership Audit"
echo "Root: $ROOT_DIR"
echo

echo "Tracked snapshot (respects .gitignore):"
read -r csharp_total_trk csharp_generated_trk csharp_manual_trk csharp_ratio_trk < <(compute_stats "$ROOT_DIR/src/Flutter" "*.cs" "0")
read -r dart_total_trk dart_generated_trk dart_manual_trk dart_ratio_trk < <(compute_stats "$ROOT_DIR/flutter_module/lib" "*.dart" "0")

print_row "src/Flutter (*.cs)" "$csharp_total_trk" "$csharp_generated_trk" "$csharp_manual_trk" "$csharp_ratio_trk"
print_row "flutter_module/lib (*.dart)" "$dart_total_trk" "$dart_generated_trk" "$dart_manual_trk" "$dart_ratio_trk"

# Additional hotspot visibility
read -r p_total_trk p_generated_trk p_manual_trk p_ratio_trk < <(compute_stats "$ROOT_DIR/flutter_module/lib/parsers" "*.dart" "0")
read -r gp_total_trk gp_generated_trk gp_manual_trk gp_ratio_trk < <(compute_stats "$ROOT_DIR/flutter_module/lib/generated/parsers" "*.dart" "0")
read -r gs_total_trk gs_generated_trk gs_manual_trk gs_ratio_trk < <(compute_stats "$ROOT_DIR/flutter_module/lib/generated/structs" "*.dart" "0")

print_row "flutter_module/lib/parsers" "$p_total_trk" "$p_generated_trk" "$p_manual_trk" "$p_ratio_trk"
print_row "flutter_module/lib/generated/parsers" "$gp_total_trk" "$gp_generated_trk" "$gp_manual_trk" "$gp_ratio_trk"
print_row "flutter_module/lib/generated/structs" "$gs_total_trk" "$gs_generated_trk" "$gs_manual_trk" "$gs_ratio_trk"

echo
echo "Workspace snapshot (includes ignored generated outputs):"
read -r csharp_total_ws csharp_generated_ws csharp_manual_ws csharp_ratio_ws < <(compute_stats "$ROOT_DIR/src/Flutter" "*.cs" "1")
read -r dart_total_ws dart_generated_ws dart_manual_ws dart_ratio_ws < <(compute_stats "$ROOT_DIR/flutter_module/lib" "*.dart" "1")

print_row "src/Flutter (*.cs)" "$csharp_total_ws" "$csharp_generated_ws" "$csharp_manual_ws" "$csharp_ratio_ws"
print_row "flutter_module/lib (*.dart)" "$dart_total_ws" "$dart_generated_ws" "$dart_manual_ws" "$dart_ratio_ws"

read -r p_total_ws p_generated_ws p_manual_ws p_ratio_ws < <(compute_stats "$ROOT_DIR/flutter_module/lib/parsers" "*.dart" "1")
read -r gp_total_ws gp_generated_ws gp_manual_ws gp_ratio_ws < <(compute_stats "$ROOT_DIR/flutter_module/lib/generated/parsers" "*.dart" "1")
read -r gs_total_ws gs_generated_ws gs_manual_ws gs_ratio_ws < <(compute_stats "$ROOT_DIR/flutter_module/lib/generated/structs" "*.dart" "1")

print_row "flutter_module/lib/parsers" "$p_total_ws" "$p_generated_ws" "$p_manual_ws" "$p_ratio_ws"
print_row "flutter_module/lib/generated/parsers" "$gp_total_ws" "$gp_generated_ws" "$gp_manual_ws" "$gp_ratio_ws"
print_row "flutter_module/lib/generated/structs" "$gs_total_ws" "$gs_generated_ws" "$gs_manual_ws" "$gs_ratio_ws"

echo
printf 'Thresholds: C# >= %s%%, Dart >= %s%%\n' "$CSHARP_THRESHOLD" "$DART_THRESHOLD"

if [[ "$ENFORCE" -eq 1 ]]; then
  if [[ "$SCOPE" == "workspace" ]]; then
    csharp_ratio_eval="$csharp_ratio_ws"
    dart_ratio_eval="$dart_ratio_ws"
  else
    csharp_ratio_eval="$csharp_ratio_trk"
    dart_ratio_eval="$dart_ratio_trk"
  fi

  csharp_ok="$(awk "BEGIN { print ($csharp_ratio_eval >= $CSHARP_THRESHOLD) ? 1 : 0 }")"
  dart_ok="$(awk "BEGIN { print ($dart_ratio_eval >= $DART_THRESHOLD) ? 1 : 0 }")"

  if [[ "$csharp_ok" -ne 1 || "$dart_ok" -ne 1 ]]; then
    echo "Ownership audit failed thresholds (scope=$SCOPE)."
    exit 1
  fi
fi
